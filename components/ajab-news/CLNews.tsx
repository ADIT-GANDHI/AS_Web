// ─── Helpers ──────────────────────────────────────────────────────────────────

const stripHtml = (v?: string | null) =>
  v ? v.replace(/<[^>]*>/g, ' ').replace(/\s+/g, ' ').trim() : '';

const toImageUrl = (v?: string) => {
  if (!v) return '';
  if (v.startsWith('http://') || v.startsWith('https://')) return v;
  if (v.startsWith('/')) return withAppBasePath(v);
  return `${NEWS_ASSET_BASE}${v.replace(/^\/+/, '')}`;
};

/** Determine if secondTitle is a subtitle phrase vs a byline name.
 *  Subtitles start with a capital "I " or contain mixed case with spaces.
 *  Bylines are typically short ALL-CAPS or proper-name strings. */
const isSubtitlePhrase = (s: string) =>
  s.startsWith('I ') ||
  (s.includes(' ') && s !== s.toUpperCase() && s.length > 20);

// ─── ToggleText ───────────────────────────────────────────────────────────────
function ToggleText({ text }: { text: string }) {
  const [expanded, setExpanded] = useState(false);
  const truncateLimit = 520;
  const isLong = text.length > truncateLimit + 20;

  if (!isLong) {
    return (
      <div className="news-text-wrap">
        <p className="leading-relaxed news-line-content">
          {text}
        </p>
      </div>
    );
  }

  const truncated = text.slice(0, truncateLimit).trim();

  return (
    <div className="news-text-wrap">
      <p className="leading-relaxed news-line-content">
        {expanded ? text : truncated}
        <span
          onClick={() => setExpanded(!expanded)}
          className="pink-text cursor-pointer hover:underline inline-block"
          style={{ marginLeft: '6px', whiteSpace: 'nowrap' }}
        >
          {expanded ? ' See less' : ' ...more'}
        </span>
      </p>
    </div>
  );
}


// ─── Section renderers ────────────────────────────────────────────────────────

/** Section 1 & 2: Full-width image with title above, heading + text below
 *  [Claude] these changes have been recommended by claude — when useSlider is true (idx=1),
 *  the section renders as a pure image carousel only (no title above, no text below).
 *  This matches PDF page 3 (Figma node 1:18663) where the second section is an image-only
 *  slider with ← → arrows and no accompanying title or body text. */
function FullWidthSection({ section, useSlider }: { section: Section; useSlider: boolean }) {
  const isSubtitle = isSubtitlePhrase(section.secondTitle);
  const hasImage = section.images.length > 0;

  // [Claude] these changes have been recommended by claude — slider section (idx=1) shows:
  // images with arrows (NO title above), then heading + text below.
  // Matches PDF page 3: the slider flows directly into its description without a title label above.
  if (useSlider) {
    return (
      <>
        <ImageSlider images={section.images} />
        <div className="news-detals-row">
          <h2 className="custom-heading-font">
            {section.title}
            {!!section.secondTitle && isSubtitle && (
              <span className="news-subtitle-italic"> {section.secondTitle}</span>
            )}
          </h2>
          {!!section.content ? (
            <ToggleText text={section.content} />
          ) : (
            <p className="leading-relaxed">No content available.</p>
          )}
        </div>
      </>
    );
  }

  return (
    <>
      {/* Title ABOVE image (Figma: "Gulshan-e-Armaan by KABIR PROJECT") */}
      {hasImage && (
        <div className="news-above-title-row">
          <p style={{ margin: 0, lineHeight: 1.3 }}>
            <span className="news-above-main-title">{section.title}</span>
            {!!section.secondTitle && !isSubtitle && (
              <>
                <span className="news-above-by-label"> by </span>
                <span className="news-above-by-value">{section.secondTitle}</span>
              </>
            )}
            {!!section.secondTitle && isSubtitle && (
              <span className="news-subtitle-italic"> {section.secondTitle}</span>
            )}
          </p>
        </div>
      )}

      {/* Single image */}
      {hasImage && (
        <div className="news-card-slider-row">
          <div className="news-banner-shadow news-image-wrap">
            <img src={section.images[0]} alt={section.title} loading="eager" decoding="async" />
          </div>
        </div>
      )}

      {/* [Claude] these changes have been recommended by claude — italic caption directly under
          the image (PDF page 3 shows "The Kabir Project team with volunteers" here). */}
      {hasImage && !!section.imageCaption && (
        <p className="news-image-caption">{section.imageCaption}</p>
      )}

      {/* Heading + body text below image */}
      <div className="news-detals-row">
        <h2 className="custom-heading-font">
          {section.title}
          {!!section.secondTitle && isSubtitle && (
            <span className="news-subtitle-italic"> {section.secondTitle}</span>
          )}
        </h2>
        {!!section.content ? (
          <ToggleText text={section.content} />
        ) : (
          <p className="leading-relaxed">No content available.</p>
        )}
      </div>
    </>
  );
}

/** Section 3+: Thumbnail (280×156) on left, title + text on right */
function ThumbnailSection({ section }: { section: Section }) {
  const isSubtitle = isSubtitlePhrase(section.secondTitle);
  const hasImage = section.images.length > 0;

  return (
    <div className="news-thumb-row">
      {hasImage && (
        <div className="news-thumb-img">
          <img src={section.images[0]} alt={section.title} loading="eager" decoding="async" />
        </div>
      )}
      <div className={`news-thumb-content${!hasImage ? ' w-full' : ''}`}>
        <h2 className="news-thumb-heading">
          {section.title}
          {!!section.secondTitle && isSubtitle && (
            <span className="news-subtitle-italic"> {section.secondTitle}</span>
          )}
          {!!section.secondTitle && !isSubtitle && (
            <>
              {' '}
              <span className="news-above-by-label">by </span>
              <span className="news-above-by-value">{section.secondTitle}</span>
            </>
          )}
        </h2>
        {!!section.content ? (
          <ToggleText text={section.content} />
        ) : (
          <p className="leading-relaxed">No content available.</p>
        )}
      </div>
    </div>
  );
}

// ─── Main Component ────────────────────────────────────────────────────────────
export default function CLAjabnews() {
  const [sections, setSections] = useState<Section[]>(() => buildSections(FIGMA_NEWS));
  const [showAll, setShowAll] = useState(false);

  useEffect(() => {
    const load = async () => {
      try {
        const res = await fetch(`${AJAB_API_BASE}/Api/news`, { cache: 'no-store' });
        if (!res.ok) return;
        const json = await res.json();
        const raw: ApiNewsItem[] = Array.isArray(json?.data) ? json.data : [];
        const built = buildSections(raw);
        if (built.length) setSections(built);
      } catch {
        /* FIGMA_NEWS fallback already in state */
      }
    };
    void load();
  }, []);

  /** How many sections to show before "See more" button */
  const VISIBLE_COUNT = 3;

  if (!sections.length)
    return (
      <div className="custom-inner-container mx-auto" style={{ paddingTop: 40, color: '#6d6e71' }}>
        No Ajab News found.
      </div>
    );

  const visibleSections = showAll ? sections : sections.slice(0, VISIBLE_COUNT);
  const hasMore = sections.length > VISIBLE_COUNT;

  return (
    <div className="custom-inner-container mx-auto">
      {visibleSections.map((section, idx) => {
        // Section 0: single full-width image
        // Section 1: slider (multiple images with arrows)
        // Section 2+: thumbnail layout
        const useSlider = idx === 1 && section.images.length > 1;
        const useThumbnail = idx >= 2;

        return (
          <article key={section.id}>
            {useThumbnail ? (
              <ThumbnailSection section={section} />
            ) : (
              <FullWidthSection section={section} useSlider={useSlider} />
            )}

            {/* Dotted separator between articles */}
            {section.showDivider && idx < visibleSections.length - 1 && (
              <div className="border-dotted-seprator" />
            )}
            {/* Separator at the end of the last visible item if more are hidden */}
            {idx === visibleSections.length - 1 && hasMore && !showAll && section.showDivider && (
              <div className="border-dotted-seprator" />
            )}
          </article>
        );
      })}

      {/* See more / See less */}
      {hasMore && (
        <div className="news-see-more-wrap">
          <button
            type="button"
            className="news-see-more-btn"
            onClick={() => setShowAll((v) => !v)}
          >
            {showAll ? 'SEE LESS' : 'SEE MORE'}
          </button>
        </div>
      )}
    </div>
  );
}

// ─── Helper: flatten news items → sections ────────────────────────────────────
function buildSections(raw: ApiNewsItem[]): Section[] {
  const items = filterNewsForPublicSite(raw);

  const all: Section[] = items.flatMap((news) => {
    const pops = (news.popup_items || []).filter(
      (p) => p?.category === 'single' || p?.category === 'multiple'
    );

    return pops.map((p, pi) => {
      const images =
        p.category === 'single'
          ? [toImageUrl(p.image)].filter(Boolean)
          : Array.isArray(p.images)
          ? p.images.map((e) => toImageUrl(e)).filter(Boolean)
          : [];

      return {
        id: `${news.id}-${pi}`,
        title: p.title || news.news_title || 'Untitled',
        secondTitle: p.second_title || news.news_second_title || '',
        content: p.content || stripHtml(news.ajab_news_content) || '',
        category: p.category as PopupCategory,
        images,
        showDivider: true, // always true; rendering logic controls visibility
        imageCaption: p.image_caption || '',
      };
    });
  });

  return all;
}
